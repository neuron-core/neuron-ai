    public function test_tool_variadic_invocation_array_properties(): void
    {
        $tool = new class () extends Tool {
            protected string $name = 'test_tool';
            protected ?string $description = 'description';
            public function __construct()
            {
                $this->addProperty(
                    new ArrayProperty(
                        name: 'colors',
                        description: "description color",
                        required: true,
                        items: new ToolProperty(
                            name: 'colors',
                            type: PropertyType::STRING,
                            description: "description color",
                            required: true,
                        )
                    )
                );
                $this->addProperty(
                    new ObjectProperty(
                        name: 'dimensions',
                        description: "description dimensions",
                        required: true,
                        properties: [
                            new ToolProperty(
                                name: 'width',
                                type: PropertyType::INTEGER,
                                description: "width",
                                required: true,
                            ),
                            new ToolProperty(
                                name: 'height',
                                type: PropertyType::INTEGER,
                                description: "height",
                                required: true,
                            ),
                        ]
                    )
                );
            }
            public function __invoke(mixed ...$data): array
            {
                return $data;
            }
        };

        $tool->setInputs([
            "colors" => ["red", "green", "red", "red", "green"],
            "dimensions" => [
                "width" => 600,
                "height" => 800,
            ],
        ]);

        $tool->execute();

        $this->assertEquals(
            '{"colors":["red","green","red","red","green"],"dimensions":{"width":600,"height":800}}',
            $tool->getResult()
        );
    }

    public function test_tool_named_parameters(): void
    {
        $tool = new class () extends Tool {
            protected string $name = 'test_tool';
            protected ?string $description = 'description';
            public function __construct()
            {
                $this->addProperty(
                    new ToolProperty(
                        name: 'prop_3',
                        type: PropertyType::BOOLEAN,
                        description: "description prop_1",
                        required: false,
                    )
                );
                $this->addProperty(
                    new ToolProperty(
                        name: 'prop_1',
                        type: PropertyType::STRING,
                        description: "description prop_1",
                        required: true,
                    )
                );
                $this->addProperty(
                    new ToolProperty(
                        name: 'prop_2',
                        type: PropertyType::INTEGER,
                        description: "description prop_2",
                        required: true,
                    )
                );
            }
            public function __invoke(int $prop_2, string $prop_1, bool $prop_3): array
            {
                return [
                    'prop_1' => $prop_1,
                    'prop_2' => $prop_2,
                    'prop_3' => $prop_3,
                ];
            }
        };

        $input = [
            'prop_1' => 'a',
            'prop_3' => false,
            'prop_2' => 3,
        ];

        $tool->setInputs($input);

        $tool->execute();

        $this->assertEquals(
            '{"prop_1":"a","prop_2":3,"prop_3":false}',
            $tool->getResult()
        );
    }

