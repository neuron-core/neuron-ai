    public function test_tool_optional_parameters(): void
    {
        $tool = new class () extends Tool {
            protected string $name = 'test_tool';
            protected ?string $description = 'description';
            public function __construct()
            {
                $this->addProperty(new ToolProperty(
                    name: 'optional_prop_1',
                    type: PropertyType::STRING,
                    description: "description",
                    required: false,
                ));
                $this->addProperty(new ToolProperty(
                    name: 'optional_prop_2',
                    type: PropertyType::STRING,
                    description: "description",
                    required: false,
                ));
                $this->addProperty(new ToolProperty(
                    name: 'optional_prop_3',
                    type: PropertyType::STRING,
                    description: "description",
                    required: false,
                ));
            }
            public function __invoke(?string $optional_prop_1, ?string $optional_prop_2, ?string $optional_prop_3): array
            {
                return [
                    'optional_prop_1' => $optional_prop_1,
                    'optional_prop_2' => $optional_prop_2,
                    'optional_prop_3' => $optional_prop_3,
                ];
            }
        };

        // Example: none of the optional parameters are provided
        $tool->setInputs([]);

        $tool->execute();

        $this->assertEquals(
            '{"optional_prop_1":null,"optional_prop_2":null,"optional_prop_3":null}',
            $tool->getResult()
        );
    }

    public function test_tool_variadic_invocation_basic_properties(): void
    {
        $tool = new class () extends Tool {
            protected string $name = 'user_manager';
            protected ?string $description = 'Update user information';
            public function __construct()
            {
                $this->addProperty(new ToolProperty(
                    name: 'lastname',
                    type: PropertyType::STRING,
                    description: "The user lastname",
                    required: false,
                ));
                $this->addProperty(new ToolProperty(
                    name: 'firstname',
                    type: PropertyType::STRING,
                    description: "The user firstname",
                    required: false,
                ));
                $this->addProperty(new ToolProperty(
                    name: 'email',
                    type: PropertyType::STRING,
                    description: "The user email",
                    required: true,
                ));
                $this->addProperty(new ToolProperty(
                    name: 'language',
                    type: PropertyType::STRING,
                    description: "The user's language",
                    required: true,
                    enum: ['fr', 'en', 'it', 'de', 'es'],
                ));
            }
            public function __invoke(mixed ...$data): array
            {
                return $data;
            }
        };

        // Example: the user's last name is not provided
        $tool->setInputs([
            "firstname" => "John",
            "email" => "john.doe@example.com",
            "language" => "fr",
        ]);

        $tool->execute();

        $this->assertEquals(
            '{"lastname":null,"firstname":"John","email":"john.doe@example.com","language":"fr"}',
            $tool->getResult()
        );
    }

    public function test_tool_variadic_invocation_object_properties(): void
    {
        $tool = new class () extends Tool {
            protected string $name = 'test_tool';
            protected ?string $description = 'description';
            public function __construct()
            {
                $this->addProperty(
                    new ObjectProperty(
                        name: 'obj_prop_1',
                        description: "description obj_prop_1",
                        required: true,
                        properties: [
                            new ToolProperty(
                                name: 'a',
                                type: PropertyType::STRING,
                                description: "description a",
                                required: true,
                            ),
                            new ToolProperty(
                                name: 'b',
                                type: PropertyType::STRING,
                                description: "description b",
                                required: true,
                            ),
                        ]
                    )
                );
                $this->addProperty(
                    new ObjectProperty(
                        name: 'obj_prop_2',
                        description: "description obj_prop_2",
                        required: false,
                        properties: [
                                new ToolProperty(
                                    name: 'x',
                                    type: PropertyType::STRING,
                                    description: "description x",
                                    required: true,
                                ),
                                new ToolProperty(
                                    name: 'y',
                                    type: PropertyType::STRING,
                                    description: "description y",
                                    required: true,
                                ),
                            ]
                    )
                );
            }
            public function __invoke(mixed ...$data): array
            {
                return $data;
            }
        };

        // Example, the second object property is not provided
        $tool->setInputs([
            "obj_prop_1" => [
                "a" => "a_input",
                "b" => "b_input",
            ],
        ]);

        $tool->execute();

        $this->assertEquals(
            '{"obj_prop_1":{"a":"a_input","b":"b_input"},"obj_prop_2":null}',
            $tool->getResult()
        );
    }

    public function test_tool_variadic_invocation_mapped_object_properties(): void
